    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Add payment  code here:

        String date = jTextField4.getText();
        String value = jTextField5.getText();
        String studentID = String.valueOf(jComboBox2.getSelectedItem());
        String teacherID = String.valueOf(jComboBox1.getSelectedItem());
        String mothId = String.valueOf(jComboBox3.getSelectedItem());

        jLabel7.setText(String.valueOf(studentMap.get(studentID)));

        if (studentID.equals("Select Student")) {
            JOptionPane.showMessageDialog(this, "Select Student", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (teacherID.equals("Select Teacher")) {
            JOptionPane.showMessageDialog(this, "Select Teacher", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (mothId.equals("Select Month")) {
            JOptionPane.showMessageDialog(this, "Select Month", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (value.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Value", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                int tId = teacherMap.get(teacherID);
                int mId = monthMap.get(mothId);
                int sId = studentMap.get(studentID);

                ResultSet resultSet = MySQL.execute("SELECT * FROM `invoice` WHERE `student_id`='" + sId + "' AND `teacher_id`='" + tId + "' AND `month_id`='" + mId + "'");
                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "Allready Paid", "Warniing", JOptionPane.WARNING_MESSAGE);
                } else {
                    try {

                        MySQL.execute("INSERT INTO `invoice`(`student_id`,`teacher_id`,`month_id`,`date`,`value`)"
                                + "VALUES ('" + sId + "','" + tId + "','" + mId + "','" + date + "','" + value + "')");
                        JOptionPane.showMessageDialog(this, "Payment Is Success", "Success", JOptionPane.PLAIN_MESSAGE);
                        loadAll();
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }
            } catch (Exception e) {
            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed

