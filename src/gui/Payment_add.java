    public void loadAll() {
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTable1.setEnabled(true);
        jButton1.setEnabled(true);
        jButton2.setEnabled(false);
        jLabel7.setText("");
        try {
            ResultSet resultSet1 = MySQL.execute("SELECT * FROM `student`");
            ResultSet resultSet2 = MySQL.execute("SELECT * FROM `teacher`");
            ResultSet resultSet3 = MySQL.execute("SELECT * FROM `month`");
            ResultSet resultSet4 = MySQL.execute("SELECT * FROM `invoice` \n"
                    + "INNER JOIN `student` ON `invoice`.student_id = `student`.id\n"
                    + "INNER JOIN `teacher` ON `invoice`.teacher_id = `teacher`.id\n"
                    + "INNER JOIN `month` ON `invoice`.month_id = `month`.id ORDER BY invoice.id ASC");

            Vector studentV = new Vector();
            Vector teacherV = new Vector();
            Vector monthV = new Vector();

            studentV.add("Select Student");
            teacherV.add("Select Teacher");
            monthV.add("Select Month");

            while (resultSet1.next()) {
                String fristName = resultSet1.getString("frist_name");
                String id = resultSet1.getString("id");
                studentV.add(fristName);

                studentMap.put(resultSet1.getString("frist_name"), resultSet1.getInt("id"));
            }
            DefaultComboBoxModel studentModel = new DefaultComboBoxModel(studentV);
            jComboBox2.setModel(studentModel);

            while (resultSet2.next()) {
                String teacherFName = resultSet2.getString("frist_name");
                String id = resultSet2.getString("id");
                teacherV.add(teacherFName);

                teacherMap.put(resultSet2.getString("frist_name"), resultSet2.getInt("id"));
            }
            DefaultComboBoxModel teacherModel = new DefaultComboBoxModel(teacherV);
            jComboBox1.setModel(teacherModel);

            while (resultSet3.next()) {
                String month = resultSet3.getString("month");
                String id = resultSet3.getString("id");
                monthV.add(month);

                monthMap.put(resultSet3.getString("month"), resultSet3.getInt("id"));
            }
            DefaultComboBoxModel monthModel = new DefaultComboBoxModel(monthV);
            jComboBox3.setModel(monthModel);

            DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
            tableModel.setRowCount(0);

            while (resultSet4.next()) {
                Vector v = new Vector();
                v.add(resultSet4.getString("id"));
                v.add(resultSet4.getString("student.frist_name"));
                v.add(resultSet4.getString("teacher.frist_name"));
                v.add(resultSet4.getString("month.month"));
                v.add(resultSet4.getString("date"));
                v.add(resultSet4.getString("value"));
                tableModel.addRow(v);
                jTable1.setModel(tableModel);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

